    private void loadHistorique() {
        Membre membres = (Membre) membre_list.getSelectedItem();

        if (membres == null) {
            return;
        }

        tmp.setText(membres.getNom());
        Date date = new Date();
        String status;
        modelH.setRowCount(0);
        try {
            for (Emprunt m : es.membreHistorique(membres)) {
                if (m == null || m.getDateRetour() == null) {
                    continue;
                }
                if (m.getDateRetour().before(date)) {
                    status = "en retard";
                } else {
                    status = "emprunte";
                }
                modelH.addRow(new Object[]{
                    m.getId(),
                    m.getLivre(),
                    m.getDateEmprunt(),
                    m.getDateRetour(),
                    status
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(Emprunt_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadLivres() {
        try {
            for (Livre l : ls.listLivres()) {
                livre_list.addItem(l);
            }
        } catch (Exception ex) {
            Logger.getLogger(Emprunt_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadMembres() {
        try {
            for (Membre m : ms.listMembres()) {
                membre_list.addItem(m);
            }
        } catch (Exception ex) {
            Logger.getLogger(Emprunt_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadEmprunt() throws Exception {
        Date date = new Date();
        String status;
        model.setRowCount(0);
        try {
            for (Emprunt e : es.listEmrunt()) {
                if (e.getDateRetour().before(date)) {
                    status = "en retard";
                } else {
                    status = "emprunte";
                }
                model.addRow(new Object[]{
                    e.getId(),
                    e.getLivre(),
                    e.getMembre(),
                    e.getDateEmprunt(),
                    e.getDateRetour(),
                    status
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(Emprunt_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void vider() {
        dateE = null;
        dateR = null;
    }

