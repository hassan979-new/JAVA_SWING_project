    private void emprunt_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emprunt_btnActionPerformed
        Livre livre = (Livre) livre_list.getSelectedItem();
        Membre membre = (Membre) membre_list.getSelectedItem();
        Date dateE = emprunt_date.getDate();
        Date dateR = retour_date.getDate();

        if (livre == null || membre == null || dateE == null || dateR == null) {
            JOptionPane.showMessageDialog(this, "Remplez vous tous les champs!");
        }
        try {
            es.empruntLivre(new Emprunt(livre, membre, dateE, dateR));
            loadEmprunt();
            vider();
        } catch (Exception ex) {
            Logger.getLogger(Emprunt_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_emprunt_btnActionPerformed

